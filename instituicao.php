			<p>A Escola Municipal América Ribeiro Lopes teve início de funcionamento na Escola Estadual Dr. José Augusto em 1943 com o 1º grau, tempo em que levava o nome de Escolas Reunidas Santos Mestres. A origem legal da escola está atrelada à Resolução SEE nº 8380/98, publicada em 21/02/1998, data em que foi municipalizado o ensino da 1ª a 4ª série, retroagindo seus efeitos a 1º de janeiro de 1998. As turmas municipalizadas continuaram funcionando no prédio da E. E. Dr. José Augusto, situada à rua Joaquim Franco, nº 47, com a denominação escola Municipal do Ensino Fundamental ( 1ª a 4ª série). </p>
			<p>A Escola Municipal do Ensino Fundamental passou a funcionar, a contar de 01/02/1999, em prédio cedido pela Escola Municipal Folhinha Verde, atendia alunos da pré-escola, situada à rua Paulo Guerra nº. 95, Centro, Entre Folhas MG, restando pendente a legalização desta mudança de prédio. Posteriormente, a Escola Municipal do Ensino Fundamental de 1ª a 4ª série passou a denominar-se Escola Municipal América Ribeiro Lopes, conforme a Lei Municipal nº 170/98, de 30 de junho de 1998 e autorizada através da portaria SEE nº 1298/98-MG, em 25/11/1998, retificada no MG de 12/11/1999 (constava erroneamente “Américo”). Em 2006, a E. M. América Ribeiro Lopes aderiu ao Ensino Fundamental de nove anos, passando a atender alunos da fase introdutória, fase I, fase II, fase III, fase IV do ensino Fundamental, no turno da manhã e tarde.</p> 
			<p>Através da portaria SEE nº 103/2014, publicada em MG de 23/01/2014, foi legalizada a mudança da Escola Municipal América Ribeiro Lopes, da rua Joaquim Franco, nº 47, Centro, em Entre Folhas  para a rua Paulo Guerra, nº 95, Centro, no período de 01/02/1999 a 11/08/2013. A partir de 12/08/2013 mudou-se para um novo prédio à rua Dr. Maninho, nº 72, Centro, Entre Folhas MG, CEP: 35324-000.</p>
